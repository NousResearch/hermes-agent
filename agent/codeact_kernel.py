# Helpers
# ---------------------------------------------------------------------------

_CRED_PATTERNS = frozenset({
    "KEY", "TOKEN", "SECRET", "PASSWORD", "CREDENTIAL", "PASSWD", "AUTH",
})

_SAFE_PREFIXES = frozenset({
    "PATH", "HOME", "USER", "LANG", "LC_", "TERM", "TMP", "TEMP", "SHELL",
    "LOGNAME", "XDG_", "PYTHONPATH", "VIRTUAL_ENV", "CONDA_", "HERMES_",
    "HERMES_CODEACT_SOCKET",
})


def _scrub_env(env: dict[str, str]) -> dict[str, str]:
    """Remove credential env vars from the subprocess environment."""
    scrubbed = {}
    for key, value in env.items():
        key_upper = key.upper()
        if any(pat in key_upper for pat in _CRED_PATTERNS):
            continue
        scrubbed[key] = value
    return scrubbed


def _format_exec_result(msg: dict) -> str:
    """Build a human-readable result string from an exec_result message."""
    parts: list[str] = []

    stdout = _truncate(msg.get("stdout", ""), _MAX_STDOUT_CHARS, "stdout")
    stderr = _truncate(msg.get("stderr", ""), _MAX_STDERR_CHARS, "stderr")
    last_value = msg.get("last_value")
    tb = msg.get("traceback", "")

    if stdout.strip():
        parts.append(stdout.rstrip())
    if last_value is not None and last_value.strip():
        parts.append(f"=> {last_value}")
    if stderr.strip():
        parts.append(f"[stderr]\n{stderr.rstrip()}")
    if tb:
        parts.append(f"[error]\n{tb.rstrip()}")

    return "\n".join(parts) if parts else "(no output)"
