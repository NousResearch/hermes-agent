import { useCallback } from 'react'

import { persistString, storedString } from './lib/storage'

export type DesktopLanguage = 'en' | 'zh'

export interface DesktopLanguageOption {
  id: DesktopLanguage
  label: string
  nativeLabel: string
}

const LANGUAGE_KEY = 'hermes-desktop-language-v1'

export const DESKTOP_LANGUAGE_OPTIONS: DesktopLanguageOption[] = [
  { id: 'en', label: 'English', nativeLabel: 'English' },
  { id: 'zh', label: 'Chinese (Simplified)', nativeLabel: '简体中文' }
]

const normalizeLanguage = (value: string | null | undefined): DesktopLanguage => {
  const raw = value?.trim().toLowerCase()

  if (raw === 'zh' || raw === 'zh-cn' || raw === 'zh-hans' || raw === 'chinese') {
    return 'zh'
  }

  return 'en'
}

const initialLanguage = () => normalizeLanguage(storedString(LANGUAGE_KEY))

export const $desktopLanguage = atom<DesktopLanguage>(initialLanguage())

function applyDocumentLanguage(language: DesktopLanguage) {
  if (typeof document === 'undefined') {
    return
  }

  document.documentElement.lang = language === 'zh' ? 'zh-CN' : 'en'
}

applyDocumentLanguage($desktopLanguage.get())

$desktopLanguage.subscribe(applyDocumentLanguage)

export function setDesktopLanguage(language: DesktopLanguage) {
  $desktopLanguage.set(language)
  persistString(LANGUAGE_KEY, language)
}

