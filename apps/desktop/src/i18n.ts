const catalogs: Record<DesktopLanguage, Record<string, string>> = { en, zh }

export function translate(key: string, language = $desktopLanguage.get(), vars?: Record<string, string | number>) {
  const template = catalogs[language][key] ?? catalogs.en[key] ?? key

  if (!vars) {
    return template
  }

  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(vars[name] ?? `{${name}}`))
}

export function useDesktopI18n() {
  const language = useStore($desktopLanguage)

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => translate(key, language, vars),
    [language]
  )

  return {
    language,
    setLanguage: setDesktopLanguage,
    t
  }
}
