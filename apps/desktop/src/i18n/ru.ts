// Russian has three plural forms (one / few / many). Pick the right one for a
// given count so generated strings read naturally:
//   1 уведомление · 2 уведомления · 5 уведомлений
const plural = (n: number, one: string, few: string, many: string): string => {
  const mod10 = n % 10
  const mod100 = n % 100
  if (mod10 === 1 && mod100 !== 11) return one
  if (mod10 >= 2 && mod10 <= 4 && (mod100 < 12 || mod100 > 14)) return few
  return many
}

