#!/usr/bin/env python3
"""
Bridge CLI — v1.0 acceptance path for the main link.

Simulates what the Electron UI would do through IPC, using CLI commands
backed by the RunBridge aggregator.  This allows manual end-to-end
acceptance without an Electron app.

Usage (via executors.cli):
    # Fixture-based acceptance (no real executor needed)
    python -m executors.cli bridge accept --scenario happy-path
    python -m executors.cli bridge accept --scenario failed
    python -m executors.cli bridge accept --scenario all

    # View aggregated views from fixture events
    python -m executors.cli bridge logs --fixture happy-path
    python -m executors.cli bridge changed-files --fixture happy-path
    python -m executors.cli bridge diff --fixture happy-path

    # IPC simulation
    python -m executors.cli bridge ipc trigger-review --main-run-id run-001
    python -m executors.cli bridge ipc trigger-qa --main-run-id run-001
"""

