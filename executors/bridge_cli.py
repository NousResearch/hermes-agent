def _run_fixture(scenario: str) -> RunResult:
    """Load a fixture and run it through RunBridge."""
    factory = _FIXTURES.get(scenario)
    if factory is None:
        print(f"Unknown scenario: {scenario}. Available: {list(_FIXTURES.keys())}", file=sys.stderr)
        sys.exit(1)

    events = factory()
    bridge = RunBridge()
    for event in events:
        bridge.ingest(event)
    return bridge.finalize()


async def cmd_accept(scenario: str = "all") -> None:
    """Run acceptance tests for the main link."""
    scenarios = list(_FIXTURES.keys()) if scenario == "all" else [scenario]

    passed = 0
    failed = 0

    for sc in scenarios:
        result = _run_fixture(sc)
        checks = _check_acceptance(result, sc)
        for chk in checks:
            if chk["ok"]:
                passed += 1
                print(f"  ✓ {chk['name']}")
            else:
                failed += 1
                print(f"  ✗ {chk['name']}: {chk.get('detail', '')}")

    print(f"\nAcceptance: {passed} passed, {failed} failed")
    if failed > 0:
        sys.exit(1)


def _check_acceptance(result: RunResult, scenario: str) -> list:
    """Run scenario-specific checks."""
    checks = []

    if scenario == "happy-path":
        checks.append({
            "name": "Run completed",
            "ok": result.status == RunStatus.COMPLETED,
            "detail": f"status={result.status.value}",
        })
        checks.append({
            "name": "Message received",
            "ok": len(result.message_parts) > 0,
            "detail": f"{len(result.message_parts)} message parts",
        })
        checks.append({
            "name": "Tool calls tracked",
            "ok": len(result.tool_calls) >= 3,
            "detail": f"{len(result.tool_calls)} tool calls",
        })
        checks.append({
            "name": "Changed files extracted (from diff)",
            "ok": len(result.changed_files) >= 3,
            "detail": f"{len(result.changed_files)} files",
        })
        checks.append({
            "name": "Changed files extracted (from tool output)",
            "ok": len([cf for cf in result.changed_files
                       if any(cf.path.endswith(ext) for ext in ['.py', '.ts', '.js'])]) >= 2,
            "detail": f"files: {[cf.path for cf in result.changed_files]}",
        })
        checks.append({
            "name": "Diff patch available",
            "ok": len(result.diff_patches) > 0,
            "detail": f"{len(result.diff_patches)} diff patches",
        })
        checks.append({
            "name": "Logs generated from tool output",
            "ok": len(result.logs) > 0,
            "detail": f"{len(result.logs)} log entries",
        })
        checks.append({
            "name": "Reasoning blocks captured",
            "ok": len(result.reasoning_blocks) > 0,
            "detail": f"{len(result.reasoning_blocks)} reasoning blocks",
        })
    elif scenario == "failed":
        checks.append({
            "name": "Run failed",
            "ok": result.status == RunStatus.FAILED,
            "detail": f"status={result.status.value}",
        })
        checks.append({
            "name": "Error summary captured",
            "ok": result.error_summary is not None and len(result.error_summary) > 0,
            "detail": f"error={result.error_summary[:50]}",
        })
        checks.append({
            "name": "Tool call tracked with error",
            "ok": any(tc.error for tc in result.tool_calls),
            "detail": f"{sum(1 for tc in result.tool_calls if tc.error)} errors",
        })

    return checks


async def cmd_logs(scenario: str = "happy-path") -> None:
    """Show aggregated logs from fixture events."""
    result = _run_fixture(scenario)
    print(f"=== Logs ({len(result.logs)} entries) ===")
    for log in result.logs:
        level_marker = {"info": " ", "warn": "⚠", "error": "✗"}.get(log.level, " ")
        print(f"  [{log.tool}] {level_marker} {log.message[:120]}")

    print(f"\n=== Tool Calls ({len(result.tool_calls)}) ===")
    for tc in result.tool_calls:
        status_icon = "✗" if tc.error else "✓"
        print(f"  {status_icon} {tc.tool_name} ({tc.duration:.2f}s)")
        if tc.output:
            print(f"      Output: {tc.output[:100]}")


async def cmd_changed_files(scenario: str = "happy-path") -> None:
    """Show changed files from fixture events."""
    result = _run_fixture(scenario)
    if not result.changed_files:
        print("No files changed in this run.")
        return

    print(f"=== Changed Files ({len(result.changed_files)}) ===")
    for cf in result.changed_files:
        icon = {"added": "A", "modified": "M", "deleted": "D"}.get(cf.status, "?")
        plus = f"+{cf.additions}" if cf.additions > 0 else ""
        minus = f"-{cf.deletions}" if cf.deletions > 0 else ""
        print(f"  {icon}  {cf.path:<40} {plus:>5} {minus:>5}")


async def cmd_diff(scenario: str = "happy-path") -> None:
    """Show unified diff from fixture events."""
    result = _run_fixture(scenario)
    if not result.diff_patches:
        print("No diff available for this run.")
        return

    print(f"=== Diff ({len(result.diff_patches)} patches) ===")
    for i, patch in enumerate(result.diff_patches):
        if i > 0:
            print("\n" + "=" * 60)
        # Show first 40 lines per patch
        lines = patch.split("\n")
        shown = lines[:40]
        if len(lines) > 40:
            shown.append(f"... ({len(lines) - 40} more lines)")
        print("\n".join(shown))


