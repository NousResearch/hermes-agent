def _stub_review_report(main_run_id: str, diff_patch: str) -> ReviewReport:
    """Heuristic stub review report (no executor invocation)."""
    review_run_id = f"review-{main_run_id}-stub"

    findings: List[ReviewFinding] = []

    if "password" in diff_patch.lower() or "secret" in diff_patch.lower() or "api_key" in diff_patch.lower():
        findings.append(ReviewFinding(
            id=f"{review_run_id}-sec-1",
            run_id=review_run_id,
            severity=Severity.HIGH,
            category=FindingCategory.SECURITY,
            title="Potential hardcoded secret",
            description="Diff contains references to 'password', 'secret', or 'api_key'. Verify these are not hardcoded credentials.",
            suggestion="Use environment variables or a secrets manager.",
        ))

    if "SELECT *" in diff_patch or 'f"' in diff_patch or "f'" in diff_patch:
        findings.append(ReviewFinding(
            id=f"{review_run_id}-sec-2",
            run_id=review_run_id,
            severity=Severity.CRITICAL,
            category=FindingCategory.SECURITY,
            title="Potential SQL injection or unsanitized string interpolation",
            description="Diff contains f-strings or SELECT * patterns that may indicate unsanitized input.",
            suggestion="Use parameterized queries or an ORM.",
        ))

    new_files = [l for l in diff_patch.split("\n") if l.startswith("+++ ")]
    test_files = [f for f in new_files if "test" in f.lower()]
    if len(new_files) > len(test_files):
        findings.append(ReviewFinding(
            id=f"{review_run_id}-test-1",
            run_id=review_run_id,
            severity=Severity.MEDIUM,
            category=FindingCategory.TEST_COVERAGE,
            title="New files may lack test coverage",
            description=f"{len(new_files)} files modified, only {len(test_files)} test files found.",
            suggestion="Add tests for new functionality.",
        ))

    if not findings:
        findings.append(ReviewFinding(
            id=f"{review_run_id}-info-1",
            run_id=review_run_id,
            severity=Severity.INFO,
            category=FindingCategory.MAINTAINABILITY,
            title="No obvious issues detected in diff",
            description="Automated heuristic scan found no patterns of concern. Manual review still recommended.",
        ))

    severity_counts = {"critical": 0, "high": 0, "medium": 0, "low": 0, "info": 0}
    for f in findings:
        severity_counts[f.severity.value] += 1

    now = datetime.datetime.now(datetime.timezone.utc)
    return ReviewReport(
        review_run_id=review_run_id,
        status=ReviewStatus.COMPLETED,
        executor="stub",
        total_findings=len(findings),
        critical_count=severity_counts["critical"],
        high_count=severity_counts["high"],
        medium_count=severity_counts["medium"],
        low_count=severity_counts["low"],
        info_count=severity_counts["info"],
        findings=findings,
        started_at=now,
        completed_at=now,
    )


def _stub_qa_report(main_run_id: str, changed_files: List[str]) -> QAReport:
    """Heuristic stub QA report (no executor invocation)."""
    now = datetime.datetime.now(datetime.timezone.utc)
    return QAReport(
        qa_run_id=f"qa-{main_run_id}-stub",
        status=QAStatus.COMPLETED,
        executor="stub",
        test_passed=len(changed_files),
        test_failed=0,
        test_skipped=0,
        test_output=f"Stub QA: verified {len(changed_files)} changed file(s) exist and are readable.",
        risks=[],
        started_at=now,
        completed_at=now,
    )


async def cmd_ipc(action: str, **kwargs) -> None:
    """Simulate IPC commands for Electron bridge testing."""
    if action == "trigger-review":
        # Aggregate diff from fixture
        bridge = RunBridge()
        for e in _fixture_happy_path():
            bridge.ingest(e)
        result = bridge.finalize()
        diff_patch = "\n".join(result.diff_patches) if result.diff_patches else ""
        # Run review via D1a-local stub
        report = _stub_review_report("run-main-001", diff_patch)
        print(json.dumps({
            "action": "review:trigger",
            "run_id": report.review_run_id,
            "status": report.status.value,
            "total_findings": report.total_findings,
            "critical": report.critical_count,
            "high": report.high_count,
            "medium": report.medium_count,
            "low": report.low_count,
            "executor": report.executor,
            "findings": [
                {"severity": f.severity.value, "category": f.category.value, "title": f.title}
                for f in report.findings[:5]
            ],
            "note": "Review run completed via stub heuristic (D1a-local stub, no executor required).",
        }, indent=2))
    elif action == "trigger-qa":
        bridge = RunBridge()
        for e in _fixture_happy_path():
            bridge.ingest(e)
        result = bridge.finalize()
        changed_files = [cf.path for cf in result.changed_files]
        report = _stub_qa_report("run-main-001", changed_files)
        print(json.dumps({
            "action": "qa:trigger",
            "run_id": report.qa_run_id,
            "status": report.status.value,
            "test_passed": report.test_passed,
            "test_failed": report.test_failed,
            "test_skipped": report.test_skipped,
            "executor": report.executor,
            "note": "QA run completed via stub (D1a-local stub, no executor required).",
        }, indent=2))
    elif action == "continue":
        thread_id = kwargs.get("thread_id", "thread-001")
        print(json.dumps({
            "action": "run:continue",
            "request": {"thread_id": thread_id, "prompt": "Fix the review findings", "previous_run_id": "run-happy-001"},
            "response": {"run_id": f"run-continue-{thread_id}"},
            "note": "New run created with conversation history from previous run.",
        }, indent=2))
    elif action == "retry":
        thread_id = kwargs.get("thread_id", "thread-001")
        print(json.dumps({
            "action": "run:retry",
            "request": {"thread_id": thread_id, "prompt": "Refactor auth module"},
            "response": {"run_id": f"run-retry-{thread_id}", "run_seq": 2},
            "note": "New run created WITHOUT history (fresh start).",
        }, indent=2))
    else:
        print(f"Unknown IPC action: {action}. Valid: trigger-review, trigger-qa, continue, retry", file=sys.stderr)
        sys.exit(1)


