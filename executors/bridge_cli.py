async def handle_bridge_command(args) -> None:
    """Dispatch bridge subcommands."""
    sub = args.bridge_subcommand
    if sub is None:
        print("Usage: bridge {accept|logs|changed-files|diff|ipc} [...]", file=sys.stderr)
        sys.exit(1)

    if sub == "accept":
        await cmd_accept(scenario=getattr(args, "scenario", "all"))
    elif sub == "logs":
        await cmd_logs(scenario=getattr(args, "fixture", "happy-path"))
    elif sub == "changed-files":
        await cmd_changed_files(scenario=getattr(args, "fixture", "happy-path"))
    elif sub == "diff":
        await cmd_diff(scenario=getattr(args, "fixture", "happy-path"))
    elif sub == "ipc":
        await cmd_ipc(
            action=getattr(args, "ipc_action", ""),
            main_run_id=getattr(args, "main_run_id", "run-main-001"),
            thread_id=getattr(args, "thread_id", "thread-001"),
        )
    else:
        print(f"Unknown bridge command: {sub}", file=sys.stderr)
        sys.exit(1)
