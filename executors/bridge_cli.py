def _fixture_happy_path() -> List[RunEvent]:
    """Simulate a successful agent run with file changes."""
    events: List[RunEvent] = []
    rid = "run-happy-001"
    ts_base = time.time()

    def _evt(t: RunEventType, payload: dict, offset: float = 0.0):
        events.append(RunEvent(
            type=t,
            payload=payload,
            seq=len(events),
            timestamp=ts_base + offset,
        ))

    # Agent starts reasoning
    _evt(RunEventType.REASONING, {"content": "Let me analyze the auth module refactoring request."}, 0.1)
    _evt(RunEventType.REASONING, {"content": "I need to: 1) read the current code 2) implement OAuth2 flow 3) add tests"}, 0.3)

    # Tool: read_file
    _evt(RunEventType.TOOL_CALL, {"tool_name": "read_file", "arguments": {"path": "services/auth.py"}}, 0.5)
    _evt(RunEventType.TOOL_RESULT, {
        "tool_name": "read_file",
        "tool_call_id": "tc-001",
        "content": "def login(username, password):\n    query = f\"SELECT * FROM users WHERE username='{username}'\"\n    ...",
        "duration": 0.15,
    }, 1.0)

    # Message to user
    _evt(RunEventType.MESSAGE, {"content": "I've read the current auth module. I'll now implement OAuth2 flow."}, 1.2)

    # Tool: write_file
    _evt(RunEventType.TOOL_CALL, {"tool_name": "write_file", "arguments": {"path": "services/auth.py"}}, 1.3)
    _evt(RunEventType.TOOL_RESULT, {
        "tool_name": "write_file",
        "tool_call_id": "tc-002",
        "content": "Wrote 142 lines to services/auth.py",
        "stdout": "File written successfully\n42 additions, 18 deletions",
        "duration": 0.05,
    }, 1.8)

    # Tool: write_file (new file)
    _evt(RunEventType.TOOL_CALL, {"tool_name": "write_file", "arguments": {"path": "services/auth_errors.py"}}, 2.0)
    _evt(RunEventType.TOOL_RESULT, {
        "tool_name": "write_file",
        "tool_call_id": "tc-003",
        "content": "Created new file services/auth_errors.py with 28 lines",
        "stdout": "File created successfully",
        "duration": 0.03,
    }, 2.3)

    # Tool: write_file (test)
    _evt(RunEventType.TOOL_CALL, {"tool_name": "write_file", "arguments": {"path": "tests/test_auth.py"}}, 2.5)
    _evt(RunEventType.TOOL_RESULT, {
        "tool_name": "write_file",
        "tool_call_id": "tc-004",
        "content": "Wrote 35 lines to tests/test_auth.py",
        "stdout": "File written successfully\n31 additions, 5 deletions",
        "duration": 0.04,
    }, 2.9)

    # Diff event (generated by adapter after run)
    diff_patch = (
        "diff --git a/services/auth.py b/services/auth.py\n"
        "--- a/services/auth.py\n"
        "+++ b/services/auth.py\n"
        "@@ -15,6 +15,8 @@\n"
        "-def login(username, password):\n"
        "-    query = f\"SELECT * FROM users WHERE username='{username}'\"\n"
        "+def login(token: str) -> User:\n"
        "+    payload = jwt.decode(token, SECRET, algorithms=['HS256'])\n"
        "+    return User.query.get(payload['sub'])\n"
        "+def refresh_token(token: str) -> str:\n"
        "+    return create_token(...)\n"
        "\n"
        "diff --git a/services/auth_errors.py b/services/auth_errors.py\n"
        "new file mode 100644\n"
        "--- /dev/null\n"
        "+++ b/services/auth_errors.py\n"
        "@@ -0,0 +1,28 @@\n"
        "+class AuthError(Exception): pass\n"
        "+class TokenExpiredError(AuthError): pass\n"
        "+class InvalidTokenError(AuthError): pass\n"
        "\n"
        "diff --git a/tests/test_auth.py b/tests/test_auth.py\n"
        "--- a/tests/test_auth.py\n"
        "+++ b/tests/test_auth.py\n"
        "@@ -10,5 +10,31 @@\n"
        "+def test_token_validation():\n"
        "+    assert login(valid_token).username == 'test'\n"
        "+def test_expired_token():\n"
        "+    with pytest.raises(TokenExpiredError):\n"
        "+        login(expired_token)\n"
    )
    _evt(RunEventType.DIFF, {"patch": diff_patch}, 3.0)

    # Run completes
    _evt(RunEventType.COMPLETED, {"summary": "Auth module refactored with OAuth2 support"}, 3.1)

    return events


def _fixture_failed_path() -> List[RunEvent]:
    """Simulate a failed agent run."""
    events: List[RunEvent] = []
    rid = "run-failed-001"
    ts_base = time.time()

    def _evt(t: RunEventType, payload: dict, offset: float = 0.0):
        events.append(RunEvent(type=t, payload=payload, seq=len(events)))

    _evt(RunEventType.MESSAGE, {"content": "Let me try to delete the root directory."}, 0.1)
    _evt(RunEventType.TOOL_CALL, {"tool_name": "execute_command", "arguments": {"command": "rm -rf /"}}, 0.3)
    _evt(RunEventType.TOOL_RESULT, {
        "tool_name": "execute_command",
        "tool_call_id": "tc-001",
        "content": "Permission denied: cannot remove root directory",
        "is_error": True,
        "stderr": "rm: /: Permission denied",
        "duration": 0.02,
    }, 0.8)
    _evt(RunEventType.FAILED, {"error_summary": "Permission denied — cannot execute destructive command"}, 1.0)

    return events


_FIXTURES = {
    "happy-path": _fixture_happy_path,
    "failed": _fixture_failed_path,
}


