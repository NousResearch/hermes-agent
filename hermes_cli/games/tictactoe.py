def _print_board(board: list) -> None:
    """Print the board state to the terminal."""
    symbols = []
    for cell in board:
        if cell == "X":
            symbols.append(" X ")
        elif cell == "O":
            symbols.append(" O ")
        else:
            symbols.append("   ")
    print(f"  {symbols[0]}|{symbols[1]}|{symbols[2]}")
    print(f"  ---+---+---")
    print(f"  {symbols[3]}|{symbols[4]}|{symbols[5]}")
    print(f"  ---+---+---")
    print(f"  {symbols[6]}|{symbols[7]}|{symbols[8]}")


class _GameHandler(http.server.BaseHTTPRequestHandler):
    """HTTP handler for the tic-tac-toe game."""

    def log_message(self, format, *args):
        # Suppress default access logs
        pass

    def do_GET(self):
        if self.path == "/" or self.path == "/index.html":
            self.send_response(200)
            self.send_header("Content-Type", "text/html; charset=utf-8")
            self.send_header("Cache-Control", "no-store")
            self.end_headers()
            self.wfile.write(_HTML_PAGE.encode("utf-8"))
        elif self.path == "/api/info":
            brain = "Agent (LLM)" if _agent_move_fn is not None else "No LLM"
            self.send_response(200)
            self.send_header("Content-Type", "application/json")
            self.end_headers()
            self.wfile.write(json.dumps({"brain": brain}).encode("utf-8"))
        elif self.path == "/api/reset":
            global _move_number
            _move_number = 0
            print("\n  -- New game started --")
            self.send_response(200)
            self.send_header("Content-Type", "application/json")
            self.end_headers()
            self.wfile.write(b'{"status":"ok"}')
        elif self.path == "/health":
            self.send_response(200)
            self.send_header("Content-Type", "application/json")
            self.end_headers()
            self.wfile.write(b'{"status":"ok"}')
        else:
            self.send_error(404)

    def do_POST(self):
        global _move_number
        if self.path == "/api/move":
            content_length = int(self.headers.get("Content-Length", 0))
            body = self.rfile.read(content_length).decode("utf-8")
            try:
                data = json.loads(body)
                board = data.get("board", [None] * 9)

                # Detect player's move (last X placed)
                _move_number += 1
                x_positions = [i for i in range(9) if board[i] == "X"]
                if x_positions:
                    last_x = x_positions[-1]
                    print(f"\n  [{_move_number}] You (X) -> {_POS_LABELS[last_x]}")

                result = self._compute_move(board)

                # Place agent's move on board copy for display
                if result["move"] is not None:
                    _move_number += 1
                    print(f"  [{_move_number}] Hermes (O) -> {_POS_LABELS[result['move']]}  [LLM]")
                    board[result["move"]] = "O"
                    _print_board(board)
                else:
                    print("  Hermes could not make a move (LLM unavailable)")
                    _print_board(board)

                # Check for game end
                winner = _check_winner(board)
                if winner == "X":
                    print("  Result: You win!")
                    _move_number = 0
                elif winner == "O":
                    print("  Result: Hermes wins!")
                    _move_number = 0
                elif all(cell is not None for cell in board):
                    print("  Result: Draw!")
                    _move_number = 0

                self.send_response(200)
                self.send_header("Content-Type", "application/json")
                self.end_headers()
                self.wfile.write(json.dumps(result).encode("utf-8"))
            except Exception as e:
                self.send_response(500)
                self.send_header("Content-Type", "application/json")
                self.end_headers()
                self.wfile.write(json.dumps({"error": str(e)}).encode("utf-8"))
        else:
            self.send_error(404)

    def _compute_move(self, board: list) -> dict:
        """Get agent's move using LLM.

        Returns dict with 'move' (int) and 'brain' ('llm'), or error.
        """
        empty = [i for i in range(9) if board[i] is None]
        if not empty:
            return {"move": None, "brain": "none"}

        if _agent_move_fn is not None:
            llm_move = _get_llm_move(board, _agent_move_fn)
            if llm_move is not None and llm_move in empty:
                return {"move": llm_move, "brain": "llm"}

        return {"move": None, "brain": "error", "error": "LLM unavailable"}


def _open_browser(url: str) -> None:
    """Open URL in the default browser, with platform-specific fallback."""
    try:
        if platform.system() == "Darwin":
            subprocess.Popen(["open", url], stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        elif platform.system() == "Linux":
            subprocess.Popen(["xdg-open", url], stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        else:
            webbrowser.open(url)
    except Exception:
        webbrowser.open(url)


def start_game(agent_move_fn: Callable = None, port: int = PORT) -> None:
    """Start the tic-tac-toe game server and open the browser.

    Args:
        agent_move_fn: Function that takes a prompt string and returns
                       the LLM response string. If None, uses random moves.
        port: Port to listen on.
    """
    global _server_instance, _agent_move_fn
    _agent_move_fn = agent_move_fn

    if _server_instance is not None:
        print(f"  Game already running at http://localhost:{port}")
        _open_browser(f"http://localhost:{port}")
        return

    socketserver.TCPServer.allow_reuse_address = True
    _server_instance = socketserver.TCPServer(("", port), _GameHandler)

    thread = threading.Thread(target=_server_instance.serve_forever, daemon=True)
    thread.start()

    url = f"http://localhost:{port}"
    brain = "Agent (LLM)" if agent_move_fn else "No LLM configured"
    print(f"  Tic Tac Toe started at {url}")
    print(f"  Opponent: {brain}")
    print(f"  Opening browser...")

    # Small delay to ensure server is ready before opening browser
    time.sleep(0.3)
    _open_browser(url)


def stop_game() -> None:
    """Stop the game server if running."""
    global _server_instance
    if _server_instance is not None:
        _server_instance.shutdown()
        _server_instance = None
