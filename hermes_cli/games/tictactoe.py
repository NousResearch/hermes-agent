# ---------------------------------------------------------------------------
# Minimax helpers (kept for potential future use)
# ---------------------------------------------------------------------------

def _check_winner(board: list) -> Optional[str]:
    """Return 'X', 'O', or None."""
    lines = [
        [0, 1, 2], [3, 4, 5], [6, 7, 8],  # rows
        [0, 3, 6], [1, 4, 7], [2, 5, 8],  # cols
        [0, 4, 8], [2, 4, 6],              # diags
    ]
    for a, b, c in lines:
        if board[a] and board[a] == board[b] == board[c]:
            return board[a]
    return None


def _minimax(board: list, is_maximizing: bool) -> int:
    winner = _check_winner(board)
    if winner == "O":
        return 1
    if winner == "X":
        return -1
    if all(cell is not None for cell in board):
        return 0

    if is_maximizing:
        best = -2
        for i in range(9):
            if board[i] is None:
                board[i] = "O"
                best = max(best, _minimax(board, False))
                board[i] = None
        return best
    else:
        best = 2
        for i in range(9):
            if board[i] is None:
                board[i] = "X"
                best = min(best, _minimax(board, True))
                board[i] = None
        return best


def _get_minimax_move(board: list) -> int:
    """Get the best move for O using minimax."""
    best_score = -2
    best_move = -1
    for i in range(9):
        if board[i] is None:
            board[i] = "O"
            score = _minimax(board, False)
            board[i] = None
            if score > best_score:
                best_score = score
                best_move = i
    return best_move


# ---------------------------------------------------------------------------
# LLM-based move
# ---------------------------------------------------------------------------

def _get_llm_move(board: list, agent_move_fn: Callable) -> Optional[int]:
    """Ask the LLM for its move. Returns cell index (0-8) or None on failure."""
    empty = [i for i in range(9) if board[i] is None]

    display = ""
    for i in range(9):
        cell = board[i] if board[i] else str(i)
        display += f" {cell} "
        if i % 3 < 2:
            display += "|"
        if i % 3 == 2 and i < 8:
            display += "\n---+---+---\n"

    prompt = (
        f"You are an expert Tic-Tac-Toe player. You are O, opponent is X.\n\n"
        f"Board (numbers = empty cells):\n{display}\n\n"
        f"Available moves: {empty}\n\n"
        f"Strategy (follow in order):\n"
        f"1. WIN: If you can get 3 in a row, take that cell\n"
        f"2. BLOCK: If opponent can win next turn, block them\n"
        f"3. CENTER: Take cell 4 if available\n"
        f"4. CORNERS: Take a corner (0, 2, 6, 8) if available\n"
        f"5. EDGES: Take an edge (1, 3, 5, 7)\n\n"
        f"Think step by step, then respond with ONLY the cell number."
    )

    try:
        response = agent_move_fn(prompt)
        if response:
            for char in response.strip():
                if char.isdigit() and int(char) in empty:
                    return int(char)
    except Exception:
        pass
    return None


