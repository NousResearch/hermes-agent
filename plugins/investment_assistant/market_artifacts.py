def _valuation_context(candidates: list[Candidate], generated_at: str) -> dict[str, Any]:
    pe_values = [_quote_num(candidate, "pe_ttm_ratio") for candidate in candidates]
    pb_values = [_quote_num(candidate, "pb_ratio") for candidate in candidates]
    pe_values = [value for value in pe_values if value and value > 0]
    pb_values = [value for value in pb_values if value and value > 0]
    pe_median = median(pe_values) if pe_values else None
    pb_median = median(pb_values) if pb_values else None
    return {
        "artifact_type": "valuation_context",
        "generated_at": generated_at,
        "source": "futu_market_snapshot",
        "peer_medians": {
            "pe_ttm": round(pe_median, 4) if pe_median is not None else None,
            "pb": round(pb_median, 4) if pb_median is not None else None,
        },
        "items": [
            _valuation_item(candidate, pe_median, pb_median)
            for candidate in candidates
        ],
    }


