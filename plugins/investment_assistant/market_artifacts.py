def _risk_scenario(
    candidate_pool: CandidatePool,
    macro_context: dict[str, Any],
    generated_at: str,
) -> dict[str, Any]:
    regime = macro_context.get("market_regime", {})
    signals = regime.get("signals", [])
    missing = _missing_exposures(candidate_pool)
    scenarios = [
        {
            "name": "theme_drawdown",
            "trigger": "Theme ETF or leading constituents break down versus SPY/QQQ.",
            "impact": "All holdings can correlate lower despite apparent single-name diversification.",
            "mitigation": "Keep sleeve sizing below policy limit and revisit candidate coverage.",
        },
        {
            "name": "earnings_reset",
            "trigger": "Upcoming earnings or guidance cut.",
            "impact": "High multiple or cycle-sensitive names may gap beyond technical levels.",
            "mitigation": "Require earnings calendar artifact before execution planning.",
        },
        {
            "name": "rates_or_volatility_shock",
            "trigger": "Bond proxies weaken, volatility proxies rise, or liquidity thins.",
            "impact": "Long-duration and high-beta candidates should receive lower weights.",
            "mitigation": "Prefer profitable leaders and raise cash in the selected map.",
        },
    ]
    if missing:
        scenarios.append(
            {
                "name": "theme_coverage_gap",
                "trigger": "Missing exposures: " + ", ".join(missing),
                "impact": "Portfolio may express only part of the intended theme.",
                "mitigation": "Expand candidates before final map approval.",
            }
        )
    return {
        "artifact_type": "risk_scenario",
        "generated_at": generated_at,
        "market_regime_signals": signals,
        "scenarios": scenarios,
    }


