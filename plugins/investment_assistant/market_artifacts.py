def build_market_artifacts(
    policy: InvestmentPolicy,
    candidate_pool: CandidatePool,
    macro_context: dict[str, Any] | None = None,
    sec_context: dict[str, Any] | None = None,
) -> dict[str, dict[str, Any]]:
    """Build structured evidence artifacts from the candidate pool.

    The V1 workflow deliberately derives these artifacts from market data and
    candidate metadata only. Current holdings are not available at this layer.
    """
    generated_at = utc_now()
    candidates = candidate_pool.candidates
    macro_context = macro_context or {}
    sec_context = sec_context or {}
    return {
        "research_trace": _research_trace(candidate_pool, generated_at),
        "market_context": _market_context(policy, candidate_pool, generated_at),
        "theme_exposure_map": _theme_exposure_map(candidate_pool, generated_at),
        "plate_constituents": _plate_constituents(candidate_pool, generated_at),
        "market_snapshot": _market_snapshot(candidates, generated_at),
        "technical_summary": _technical_summary(candidates, generated_at),
        "liquidity_context": _liquidity_context(candidates, generated_at),
        "options_surface": _options_surface(candidates, policy, generated_at),
        "sec_filings_context": _sec_filings_context(candidates, sec_context, generated_at),
        "fundamental_quality": _fundamental_quality(candidates, generated_at, sec_context),
        "valuation_context": _valuation_context(candidates, generated_at),
        "earnings_event_calendar": _earnings_event_calendar(candidates, generated_at, sec_context),
        "correlation_and_diversification": _correlation_and_diversification(candidates, generated_at),
        "benchmark_context": _benchmark_context(candidate_pool, macro_context, generated_at),
        "market_regime": _market_regime(macro_context, generated_at),
        "positioning_and_sentiment": _positioning_and_sentiment(candidates, generated_at),
        "analyst_revision_context": _analyst_revision_context(candidates, generated_at),
        "risk_scenario": _risk_scenario(candidate_pool, macro_context, generated_at),
        "risk_flags": _risk_flags(candidates, generated_at, sec_context),
    }


def _research_trace(candidate_pool: CandidatePool, generated_at: str) -> dict[str, Any]:
    return {
        "artifact_type": "research_trace",
        "generated_at": generated_at,
        "theme": candidate_pool.theme,
        "discovery_thesis": candidate_pool.discovery_thesis,
        "search_queries": list(candidate_pool.search_queries),
        "data_asof": dict(candidate_pool.data_asof),
        "sources": [
            source.model_dump(mode="json")
            for source in candidate_pool.research_trace
        ],
        "candidate_source_ids": {
            candidate.symbol: list(candidate.discovery_data.source_ids)
            for candidate in candidate_pool.candidates
        },
        "warnings": [
            "Research trace is generated by the discovery agent and still requires downstream market/fundamental validation."
        ],
    }


def enrich_candidate_pool_with_sec_data(
    candidate_pool: CandidatePool,
    sec_context: dict[str, Any],
) -> CandidatePool:
    """Return a candidate pool with candidate-level SEC/fundamental/event summaries."""

    sec_status = _sec_status(sec_context)
    sec_source = _sec_source(sec_context)
    enriched: list[Candidate] = []
    for candidate in candidate_pool.candidates:
        sec_item = _sec_item(sec_context, candidate.symbol)
        filings = sec_item.get("filings", {}) if isinstance(sec_item, dict) else {}
        sec_data = SecData(
            source=sec_source,
            source_status=sec_item.get("source_status", sec_status),
            ticker=sec_item.get("ticker"),
            cik=sec_item.get("cik"),
            company_name=sec_item.get("company_name"),
            industry=sec_item.get("industry"),
            fiscal_year_end=sec_item.get("fiscal_year_end"),
            filer_category=sec_item.get("filer_category"),
            latest_10k=filings.get("latest_10k"),
            latest_10q=filings.get("latest_10q"),
            latest_8k=filings.get("latest_8k"),
            event_context=sec_item.get("event_context", {}),
            risk_flags=sec_item.get("risk_flags", []),
            error=sec_item.get("error"),
        )
        fundamental_item = _fundamental_quality_item(candidate, sec_item)
        fundamental_data = FundamentalData(
            source_status=fundamental_item.get("data_status", "not_available"),
            sec_source_status=sec_status,
            numeric_source=fundamental_item.get("numeric_source", "not_available"),
            numeric_llm_generated=bool(fundamental_item.get("numeric_llm_generated", False)),
            net_profit=fundamental_item.get("net_profit"),
            eps=fundamental_item.get("eps"),
            net_asset_per_share=fundamental_item.get("net_asset_per_share"),
            dividend_yield_ttm=fundamental_item.get("dividend_yield_ttm"),
            ttm_revenue=fundamental_item.get("ttm_revenue"),
            ttm_net_income=fundamental_item.get("ttm_net_income"),
            gross_profit=fundamental_item.get("gross_profit"),
            operating_income=fundamental_item.get("operating_income"),
            total_assets=fundamental_item.get("total_assets"),
            total_liabilities=fundamental_item.get("total_liabilities"),
            shareholders_equity=fundamental_item.get("shareholders_equity"),
            debt_to_assets=fundamental_item.get("debt_to_assets"),
            roe=fundamental_item.get("roe"),
            net_margin=fundamental_item.get("net_margin"),
            quality_score=fundamental_item.get("quality_score"),
        )
        event_item = _earnings_event_item(candidate, sec_item)
        event_data = EventData(
            source_status=event_item.get("source_status", "not_available"),
            next_earnings_date=event_item.get("next_earnings_date"),
            days_to_earnings=event_item.get("days_to_earnings"),
            event_risk_level=event_item.get("event_risk_level", "unknown"),
            latest_10k=event_item.get("latest_10k"),
            latest_10q=event_item.get("latest_10q"),
            latest_8k=event_item.get("latest_8k"),
            latest_periodic_filing_date=event_item.get("latest_periodic_filing_date"),
            periodic_filing_age_days=event_item.get("periodic_filing_age_days"),
            periodic_filing_stale=event_item.get("periodic_filing_stale"),
            known_risk_tags=event_item.get("known_risk_tags", []),
        )
        risk_tags = sorted(set(candidate.risk_tags + sec_data.risk_flags))
        enriched.append(
            candidate.model_copy(
                update={
                    "sec_data": sec_data,
                    "fundamental_data": fundamental_data,
                    "event_data": event_data,
                    "risk_tags": risk_tags,
                }
            )
        )
    return candidate_pool.model_copy(update={"candidates": enriched})


def reflect_candidate_pool(
    policy: InvestmentPolicy,
    candidate_pool: CandidatePool,
    artifacts: dict[str, dict[str, Any]],
) -> CandidatePoolReflection:
    """Run a constrained reflection pass over generated market artifacts."""
    checks = [
        "current portfolio was not read before candidate generation",
        "candidate additions must come from market artifacts, not LLM memory",
        "portfolio maps may only use symbols in the enriched candidate_pool",
        "coverage requirements from theme discovery are preserved for architect omission audits",
    ]
    missing_exposure = _missing_exposures(candidate_pool)
    stale_data = _stale_data(policy, candidate_pool, artifacts)
    weak_sources = [
        candidate.symbol
        for candidate in candidate_pool.candidates
        if len(set(candidate.source_tags or [candidate.source])) < 2
    ]
    diversification = artifacts.get("correlation_and_diversification", {})
    diversification_score = diversification.get("diversification_score")
    recommended_actions: list[dict[str, Any]] = []
    if missing_exposure:
        recommended_actions.append(
            {
                "action": "expand_theme_keywords",
                "reason": "Candidate pool does not cover all expected theme exposures.",
                "missing_exposure": missing_exposure,
            }
        )
    if not _has_plate_source(candidate_pool):
        recommended_actions.append(
            {
                "action": "fetch_futu_plate_constituents",
                "reason": "Futu plate/constituent data is not yet present in the artifact set.",
            }
        )
    if policy.allow_options and stale_data:
        recommended_actions.append(
            {
                "action": "refresh_options_surface",
                "reason": "Options overlay was allowed, but option surface data is incomplete.",
            }
        )
    if isinstance(diversification_score, int | float) and diversification_score < 0.45:
        recommended_actions.append(
            {
                "action": "expand_theme_keywords",
                "reason": "Candidate pool appears highly correlated; add adjacent exposures before finalizing maps.",
                "diversification_score": diversification_score,
            }
        )

    warnings = list(candidate_pool.warnings)
    if len(candidate_pool.candidates) < 3:
        warnings.append("Candidate pool has fewer than 3 symbols; generated maps may be too concentrated.")
    if weak_sources:
        warnings.append("Some candidates have weak source diversity.")
    if isinstance(diversification_score, int | float) and diversification_score < 0.45:
        warnings.append("Candidate pool has low diversification score.")

    coverage_ok = len(candidate_pool.candidates) >= 3 and not missing_exposure
    return CandidatePoolReflection(
        coverage_ok=coverage_ok,
        missing_exposure=missing_exposure,
        stale_data=stale_data,
        weak_sources=weak_sources,
        recommended_actions=recommended_actions,
        checks=checks,
        warnings=warnings,
    )


def _market_context(
    policy: InvestmentPolicy,
    candidate_pool: CandidatePool,
    generated_at: str,
) -> dict[str, Any]:
    return {
        "artifact_type": "market_context",
        "theme": candidate_pool.theme,
        "generated_at": generated_at,
        "discovery_thesis": candidate_pool.discovery_thesis,
        "coverage_requirements": [
            requirement.model_dump(mode="json")
            for requirement in candidate_pool.coverage_requirements
        ],
        "excludes_current_portfolio": True,
        "policy": policy.model_dump(mode="json"),
        "sources": candidate_pool.generated_from,
        "candidate_count": len(candidate_pool.candidates),
        "symbols": [candidate.symbol for candidate in candidate_pool.candidates],
    }


def _theme_exposure_map(candidate_pool: CandidatePool, generated_at: str) -> dict[str, Any]:
    if candidate_pool.coverage_requirements:
        exposures = []
        eligible_symbols = {
            candidate.symbol.upper()
            for candidate in candidate_pool.candidates
            if candidate.eligible_for_portfolio
        }
        for requirement in candidate_pool.coverage_requirements:
            symbols = [symbol for symbol in requirement.candidate_symbols if symbol.upper() in eligible_symbols]
            exposures.append(
                {
                    "exposure_key": requirement.key,
                    "label": requirement.name,
                    "priority": requirement.priority,
                    "thesis": requirement.thesis,
                    "status": "covered" if len(symbols) >= requirement.min_candidates else "missing",
                    "candidate_symbols": symbols,
                    "must_consider_symbols": requirement.must_consider_symbols,
                    "evidence_needed": requirement.evidence_needed,
                }
            )
        return {
            "artifact_type": "theme_exposure_map",
            "theme": candidate_pool.theme,
            "generated_at": generated_at,
            "exposures": exposures,
            "source": "theme_discovery_coverage_requirements",
        }

    return {
        "artifact_type": "theme_exposure_map",
        "theme": candidate_pool.theme,
        "generated_at": generated_at,
        "exposures": [],
        "source": "theme_discovery_coverage_requirements",
        "warnings": [
            "Theme exposure map is unavailable because discovery returned no coverage_requirements."
        ],
    }


def _plate_constituents(candidate_pool: CandidatePool, generated_at: str) -> dict[str, Any]:
    has_plate_source = _has_plate_source(candidate_pool)
    memberships: dict[str, dict[str, Any]] = {}
    for candidate in candidate_pool.candidates:
        for membership in candidate.plate_memberships:
            plate_code = membership.get("plate_code", "")
            if not plate_code:
                continue
            plate = memberships.setdefault(
                plate_code,
                {
                    "plate_code": plate_code,
                    "plate_name": membership.get("plate_name", ""),
                    "plate_type": membership.get("plate_type", ""),
                    "constituents": [],
                },
            )
            plate["constituents"].append(
                {
                    "symbol": candidate.symbol,
                    "name": candidate.name,
                    "role_hint": candidate.theme_role,
                }
            )
    return {
        "artifact_type": "plate_constituents",
        "theme": candidate_pool.theme,
        "generated_at": generated_at,
        "source_status": "available" if has_plate_source else "not_wired",
        "source": "futu_plate_stock" if has_plate_source else None,
        "plates": list(memberships.values()),
        "constituents": [
            {
                "symbol": candidate.symbol,
                "name": candidate.name,
                "role_hint": candidate.theme_role,
                "source_tags": candidate.source_tags,
                "plate_memberships": candidate.plate_memberships,
            }
            for candidate in candidate_pool.candidates
        ],
        "warnings": []
        if has_plate_source
        else ["Futu plate/constituent adapter is not wired into this MVP build."],
    }


