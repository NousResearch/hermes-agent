def _missing_exposures(candidate_pool: CandidatePool) -> list[str]:
    if candidate_pool.coverage_requirements:
        eligible_symbols = {
            candidate.symbol.upper()
            for candidate in candidate_pool.candidates
            if candidate.eligible_for_portfolio
        }
        missing = []
        for requirement in candidate_pool.coverage_requirements:
            if requirement.priority == "optional":
                continue
            candidates = {symbol.upper() for symbol in requirement.candidate_symbols}
            must_consider = {symbol.upper() for symbol in requirement.must_consider_symbols}
            expected = must_consider or candidates
            if expected and not expected.intersection(eligible_symbols):
                missing.append(requirement.name or requirement.key)
        return missing

    return []


def _stale_data(
    policy: InvestmentPolicy,
    candidate_pool: CandidatePool,
    artifacts: dict[str, dict[str, Any]],
) -> list[str]:
    stale = []
    if not candidate_pool.candidates:
        stale.append("candidate_pool")
    if policy.allow_options:
        options = artifacts.get("options_surface", {})
        option_items = options.get("items", [])
        if not option_items or not any(item.get("has_option_data") for item in option_items):
            stale.append("options_surface")
    sec_filings = artifacts.get("sec_filings_context", {})
    if sec_filings.get("source_status") in {"not_available", "not_configured", "unavailable"}:
        stale.append("sec_filings_context")
    if any(
        item.get("event_context", {}).get("periodic_filing_stale")
        for item in sec_filings.get("items", [])
    ):
        stale.append("sec_periodic_filings")
    return stale


