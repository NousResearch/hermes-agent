def _num(value: Any) -> float | None:
    try:
        number = float(value)
    except (TypeError, ValueError):
        return None
    if math.isnan(number):
        return None
    return number


def _quality_score(candidate: Candidate) -> float | None:
    net_profit = _quote_num(candidate, "net_profit")
    eps = _quote_num(candidate, "earning_per_share")
    market_cap = _quote_num(candidate, "total_market_val")
    if net_profit is None and eps is None and market_cap is None:
        return None
    score = 0.35
    if net_profit and net_profit > 0:
        score += 0.25
    if eps and eps > 0:
        score += 0.20
    if market_cap and market_cap > 10_000_000_000:
        score += 0.10
    if (_technical_num(candidate, "relative_strength_60d") or 0.0) >= 65:
        score += 0.10
    return round(min(1.0, score), 4)


def _quality_score_with_sec(candidate: Candidate, fundamentals: dict[str, Any]) -> float | None:
    base = _quality_score(candidate)
    if not fundamentals:
        return base
    score = base if base is not None else 0.35
    ttm_net_income = _num(fundamentals.get("ttm_net_income"))
    net_margin = _num(fundamentals.get("net_margin"))
    debt_to_assets = _num(fundamentals.get("debt_to_assets"))
    if ttm_net_income and ttm_net_income > 0:
        score += 0.08
    if net_margin and net_margin > 0.10:
        score += 0.04
    if debt_to_assets is not None and debt_to_assets < 0.65:
        score += 0.03
    return round(min(1.0, score), 4)


