def _sentiment_signal(candidate: Candidate) -> str:
    volume_ratio = _quote_num(candidate, "volume_ratio")
    iv = _options_num(candidate, "avg_implied_volatility")
    bid_ask = _quote_num(candidate, "bid_ask_ratio")
    if iv and iv >= 80:
        return "high_option_uncertainty"
    if volume_ratio and volume_ratio >= 1.8 and (_technical_num(candidate, "relative_strength_60d") or 0.0) >= 65:
        return "momentum_attention"
    if bid_ask and bid_ask <= -20:
        return "orderbook_pressure"
    return "neutral"
