"""MemPalace memory provider plugin for Hermes.

Local, API-key-free memory using ChromaDB + AAAK layered recall.
Wings auto-route exchanges to project-scoped memory stores.
L0+L1 wake-up context injects identity + essential story every session.

Config:
  Palace path: ~/.mempalace/palace  (or override via $HERMES_HOME/mempalace.json)
  Wing config: ~/.mempalace/wing_config.json  (generated by `mempalace init`)
  Identity:    ~/.mempalace/identity.txt       (generated by `mempalace init`)

Run `mempalace init <your-project-dir>` to set up wings and identity.
"""

