def _classify_wing_from_config(user_msg: str, asst_msg: str, wing_config: Dict[str, Any]) -> str:
    """Keyword-score both messages against loaded wing_config and return best-matching wing."""
    if not wing_config:
        return "wing_general"
    combined = (user_msg + " " + asst_msg).lower()
    scores: Dict[str, int] = {}
    for wing_name, wing_cfg in wing_config.items():
        keywords = wing_cfg.get("keywords", [])
        score = sum(1 for kw in keywords if kw in combined)
        if score > 0:
            scores[wing_name] = score
    return max(scores, key=scores.get) if scores else "wing_general"


def _classify_hall(user_msg: str, asst_msg: str) -> str:
    """Return hall name based on dominant content pattern."""
    combined = (user_msg + " " + asst_msg).lower()
    scores: Dict[str, int] = {}
    for hall, patterns in HALL_PATTERNS.items():
        score = sum(1 for p in patterns if p in combined)
        if score > 0:
            scores[hall] = score
    return max(scores, key=scores.get) if scores else "hall_facts"


def _make_room_name(user_msg: str, asst_msg: str) -> str:
    """Slugify the first 3 meaningful words as a room name."""
    combined = (user_msg + " " + asst_msg)[:500].lower()
    words = re.findall(r"\b[a-z][a-z0-9]*\b", combined)
    words = [w for w in words if w not in _STOP_WORDS and len(w) > 2]
    slug = "-".join(words[:3]) if words else "general"
    return slug[:50]


def _extract_text_content(content: Any) -> str:
    """Flatten message content (string or content-block list) to plain text."""
    if isinstance(content, str):
        return content
    if isinstance(content, list):
        parts = []
        for block in content:
            if isinstance(block, dict):
                if block.get("type") == "text":
                    parts.append(block.get("text", ""))
            elif isinstance(block, str):
                parts.append(block)
        return " ".join(parts)
    return str(content) if content else ""


