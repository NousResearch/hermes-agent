"""In-turn compaction-stats whole-tail-replay kept-tail fix (2026-06-26).

Guards the fix for the silent two-line degrade of the in-turn granular announce:
``build_inturn_stats`` measured ``folded`` pre-side but ``kept`` comp-side, so
``validate()``'s PRE identity failed once LCM sanitized the kept tail. The fix
finds the kept-tail boundary by replaying the REAL LCM sanitizer over candidate
``messages[cut:]`` slices (tail-global drop/stub reproduced correctly), then
measures ``folded`` AND ``kept_pre`` both pre-side.

The fixture (``inturn_reconcile_sanitized_shape.json``) is generated by
``gen_inturn_reconcile_fixture.py`` from the real sanitizer; ``true_kept_count``
is an independent integer so the oracle is non-circular.
"""
from __future__ import annotations

import json
import os

import pytest

from agent.compaction_stats import (
    build_inturn_stats,
    find_inturn_kept_cut,
    _inturn_norm,
)
from agent.model_metadata import estimate_messages_tokens_rough as _est

_FIX = os.path.join(os.path.dirname(__file__), "fixtures", "inturn_reconcile_sanitized_shape.json")


def _load_fixture():
    with open(_FIX, encoding="utf-8") as fh:
        return json.load(fh)


def _sanitizer():
    from plugins.context_engine.lcm.engine import LCMEngine
    eng = LCMEngine.__new__(LCMEngine)
    return lambda ms: eng._sanitize_active_context_messages(list(ms))


def _comp_kept(comp):
    # comp-side kept tail = non-system, non-summary rows
    return [m for m in comp if m.get("role") != "system" and not m.get("_lcm_summary")]


# ───────────────────────── core RED→GREEN ─────────────────────────

def test_inturn_replay_finds_true_cut():
    """kept_pre_messages == the generator's recorded TRUE count (not 0, not derived)."""
    fx = _load_fixture()
    san = _sanitizer()
    stats = build_inturn_stats(
        messages=fx["messages"], compressed=fx["compressed"], estimator=_est,
        engine_is_lcm=True, sanitize=san, fresh_tail_count=fx["fresh_tail_count"],
    )
    ok, why = stats.validate()
    assert ok, why
    assert stats.kept_pre_messages == fx["true_kept_count"]
    assert stats.kept_pre_messages != 0


def test_inturn_reconciles_sanitized_stubbed_tail():
    """PRE identity reconciles (msg exact, tokens within tolerance) on the real-sanitizer fixture."""
    fx = _load_fixture()
    san = _sanitizer()
    stats = build_inturn_stats(
        messages=fx["messages"], compressed=fx["compressed"], estimator=_est,
        engine_is_lcm=True, sanitize=san, fresh_tail_count=fx["fresh_tail_count"],
    )
    # message axis exact
    assert stats.cleared_count + stats.folded_count + stats._kept_pre_messages == stats.pre_messages
    # token axis within tolerance
    ok, why = stats.validate()
    assert ok, why


def test_inturn_old_mixed_side_now_reconciles_via_a_floor():
    """Without the sanitizer (the fallback path), the A-floor single-walk partition
    now RECONCILES this real fixture shape (the old mixed-side bug is fixed) and
    flags ``approx_attribution`` so the caller labels + watches it. The aligned
    path (with sanitizer) remains the EXACT, non-approx path (see tests below)."""
    fx = _load_fixture()
    stats = build_inturn_stats(
        messages=fx["messages"], compressed=fx["compressed"], estimator=_est,
        engine_is_lcm=True,  # no sanitize / fresh_tail → A-floor fallback
    )
    ok, why = stats.validate()
    assert ok, why                         # A-floor reconciles by construction
    assert stats.approx_attribution is True
    # exhaustive pre-side partition
    assert stats.folded_count + stats._kept_pre_messages == stats.pre_messages


# ───────────────────────── uniqueness / determinism (pass-4 RC#1/#2) ──────────

def test_inturn_replay_rejects_neighbor_cuts():
    """accept-on-exact-match is only a gate if neighbor cuts diverge."""
    fx = _load_fixture()
    san = _sanitizer()
    msgs = fx["messages"]
    comp_kept = _comp_kept(fx["compressed"])
    cut = find_inturn_kept_cut(msgs, comp_kept, san, fx["fresh_tail_count"])
    assert cut == len(msgs) - fx["true_kept_count"]
    target = _inturn_norm(comp_kept)
    assert _inturn_norm(san(msgs[cut - 1:])) != target
    assert _inturn_norm(san(msgs[cut + 1:])) != target


def test_inturn_replay_byte_equals_comp_kept():
    """The replay at the found cut reproduces comp_kept exactly, INCLUDING the stub row."""
    fx = _load_fixture()
    san = _sanitizer()
    msgs = fx["messages"]
    comp_kept = _comp_kept(fx["compressed"])
    cut = find_inturn_kept_cut(msgs, comp_kept, san, fx["fresh_tail_count"])
    replayed = san(msgs[cut:])
    assert _inturn_norm(replayed) == _inturn_norm(comp_kept)
    # the stub row is present in the matched tail
    assert any(m.get("role") == "tool" and "earlier conversation" in str(m.get("content", ""))
               for m in replayed)


