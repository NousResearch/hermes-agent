def test_inturn_budget_trimmed_tail_aligns():
    """The §0.5 budget-trimmed (shorter) kept tail finds its interior cut and PASSES."""
    san = _sanitizer()

    def usr(i):
        return {"role": "user", "content": f"user {i} {'w' * 30}"}

    def at(i):
        return {"role": "assistant", "content": f"<thinking>t{i}</thinking>Visible {i}."}

    pre = [usr(i) if i % 2 else at(i) for i in range(200)]
    # the engine emitted only the last 20 rows (budget shortened it below fresh_tail=32)
    kept_raw = pre[-20:]
    comp = [{"role": "system", "content": "anchor"},
            {"role": "assistant",
             "content": "[Recent Summary (d0, node 1)] folded [Expand for details: x]",
             "_lcm_summary": True}] + san(kept_raw)
    stats = build_inturn_stats(
        messages=pre, compressed=comp, estimator=_est,
        engine_is_lcm=True, sanitize=san, fresh_tail_count=32,
    )
    ok, why = stats.validate()
    assert ok, why
    assert stats.kept_pre_messages == 20  # the true interior cut, not 32


