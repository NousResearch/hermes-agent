const LOGO_GRADIENT = [0, 0, 1, 1, 2, 2] as const
const CADUC_GRADIENT = [2, 2, 1, 1, 0, 0, 1, 1, 2, 2, 3, 3, 3, 3, 3] as const

const colorize = (art: string[], gradient: readonly number[], c: ThemeColors): RichLine[] => {
  const p = [c.primary, c.accent, c.border, c.muted]

  return art.map((text, i) => [[p[gradient[i]!] ?? c.muted, text]])
}

export const LOGO_WIDTH = Math.max(...LOGO_ART.map(line => line.length))
export const CADUCEUS_WIDTH = Math.max(...CADUCEUS_ART.map(line => line.length))

export const logo = (c: ThemeColors, customLogo?: string): RichLine[] =>
  customLogo ? parseRichMarkup(customLogo) : colorize(LOGO_ART, LOGO_GRADIENT, c)

export const caduceus = (c: ThemeColors, customHero?: string): RichLine[] =>
  customHero ? parseRichMarkup(customHero) : colorize(CADUCEUS_ART, CADUC_GRADIENT, c)

/** Compute the rendered width of a line (sum of all segment text lengths). */
const lineWidth = (segments: [string, string][]) =>
  segments.reduce((m, [, t]) => m + t.length, 0)

/** Compute the maximum rendered width across all lines. */
export const artWidth = (lines: RichLine[]) =>
  lines.reduce((m, segs) => Math.max(m, lineWidth(segs)), 0)

/** A line of art: an array of [color, text] segments that render on one row. */
export type RichLine = [string, string][]
