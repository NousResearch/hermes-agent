import type { ThemeColors } from './theme.js'

/**
 * Parse Rich markup into lines, each line being an array of [color, text] segments.
 * Uses a stack-based parser to handle nested/sequential colour tags like
 * `[#073642][#2aa198]...[/]...[/]` — the regex approach can't handle this
 * because non-greedy `[\s\S]*?` matches the first `[/]` it finds, treating
 * the inner `[#2aa198]` as literal text.
 */
export function parseRichMarkup(markup: string): RichLine[] {
  const lines: RichLine[] = []

  for (const raw of markup.split('\n')) {
    const trimmed = raw.trimEnd()

    if (!trimmed) {
      lines.push([['', ' ']])
      continue
    }

    const segments: [string, string][] = []
    const stack: string[] = ['']  // default colour at bottom
    let buf = ''
    let i = 0

    while (i < trimmed.length) {
      // Check for opening colour tag: [#XXXXXX]
      if (trimmed[i] === '[' && i + 1 < trimmed.length && trimmed[i + 1] === '#') {
        const closeBracket = trimmed.indexOf(']', i)
        if (closeBracket !== -1) {
          // Flush current buffer with current colour
          if (buf) {
            segments.push([stack[stack.length - 1]!, buf])
            buf = ''
          }
          const color = trimmed.slice(i + 1, closeBracket)
          stack.push(color)
          i = closeBracket + 1
          continue
        }
      }

      // Check for closing tag: [/]
      if (trimmed[i] === '[' && trimmed.slice(i, i + 3) === '[/]') {
        if (buf) {
          segments.push([stack[stack.length - 1]!, buf])
          buf = ''
        }
        if (stack.length > 1) {
          stack.pop()
        }
        i += 3
        continue
      }

      buf += trimmed[i]
      i++
    }

    // Flush remaining buffer
    if (buf) {
      segments.push([stack[stack.length - 1]!, buf])
    }

    if (segments.length === 0) {
      segments.push(['', trimmed])
    }

    lines.push(segments)
  }

  return lines
}

